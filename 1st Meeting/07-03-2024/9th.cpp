//Q.9 Write a program to find the cir_pridhi of a circle with radius 10 in C++.

#include <iostream>
using namespace std;
	
int main() {
    double radius = 10.0;
    double cir_pridhi = 2 * 3.14159 * radius;

    cout << "Cir_pridhi of the circle with radius 10 is: " << cir_pridhi << endl;

    return 0;
}
